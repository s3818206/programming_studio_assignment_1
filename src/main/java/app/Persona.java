package app;

public class Persona {
    private String name;
    private String quote;
    private String description;
    private int attributeAge;
    private String attributeGender;
    private String attributeEthnicity;
    private String background;
    private String needs;
    private String goals;
    private String skillsExperience;
    private String imageLink;

    // Getter and Setter methods for each field

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getQuote() {
        return quote;
    }

    public void setQuote(String quote) {
        this.quote = quote;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    public int getAttributeAge() {
        return attributeAge;
    }

    public void setAttributeAge(int attributeAge) {
        this.attributeAge = attributeAge;
    }

    public String getAttributeGender() {
        return attributeGender;
    }

    public void setAttributeGender(String attributeGender) {
        this.attributeGender = attributeGender;
    }

    public String getAttributeEthnicity() {
        return attributeEthnicity;
    }

    public void setAttributeEthnicity(String attributeEthnicity) {
        this.attributeEthnicity = attributeEthnicity;
    }

    public String getBackground() {
        return background;
    }

    public void setBackground(String background) {
        this.background = background;
    }

    public String getNeeds() {
        return needs;
    }

    public void setNeeds(String needs) {
        this.needs = needs;
    }

    public String getGoals() {
        return goals;
    }

    public void setGoals(String goals) {
        this.goals = goals;
    }

    public String getSkillsExperience() {
        return skillsExperience;
    }

    public void setSkillsExperience(String skillsExperience) {
        this.skillsExperience = skillsExperience;
    }

    public String getImageLink() {
        return imageLink;
    }

    public void setImageLink(String imageLink) {
        this.imageLink = imageLink;
    }
}

